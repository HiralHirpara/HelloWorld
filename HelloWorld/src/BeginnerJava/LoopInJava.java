package BeginnerJava;

public class LoopInJava {

	public static void main(String[] args) {
		
		// How to iterate number from 1 to 10 in java using "FOR" loop
		System.out.println(":::Print FOR loop data:::");
		for(int f = 1;f<=10;f++) {
			System.out.println(f);
		}
		
		// How to iterate number from 1 to 10 in java using "WHILE" loop
		System.out.println(":::Print WHILE loop data:::");
		int w = 1;
		while(w <= 10) {
			System.out.println(w);
			w++;
		}
	}

}
